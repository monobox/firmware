void sampleplayer_init()
{
    // Expected config:
    // APB1 Bus clock: 80MHz
    // TIM6 PSC=10, CNTR=1000, TRGO=update
    // DMA1 CH3 byte/byte
    HAL_TIM_Base_Start(&htim6);
    HAL_DAC_Start(&hdac1, DAC_CHANNEL_1);

    // Slowly ramp up to avoid clicks
    for (uint16_t i = 0 ; i < 127 ; ++i) {
        HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_8B_R, i);
        HAL_Delay(1);
    }

}

void sampleplayer_play_click()
{
    HAL_DAC_Stop_DMA(&hdac1, DAC_CHANNEL_1);
    HAL_DAC_Start_DMA(&hdac1, DAC_CHANNEL_1, (uint32_t*)sample_click, sizeof(sample_click), DAC_ALIGN_8B_R);
}

void sampleplayer_play_bleep()
{
    HAL_DAC_Stop_DMA(&hdac1, DAC_CHANNEL_1);
    HAL_DAC_Start_DMA(&hdac1, DAC_CHANNEL_1, (uint32_t*)sample_bleep, sizeof(sample_bleep), DAC_ALIGN_8B_R);
}
